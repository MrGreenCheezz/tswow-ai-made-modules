const assert = require("node:assert/strict");
const fs = require("node:fs");
const path = require("node:path");

const MODULE_DIR = path.resolve(__dirname, "..");
const PATCH_DIR = path.join(MODULE_DIR, "datascripts", "data", "PatchForBuildings");
const DBC_PATH = path.join(PATCH_DIR, "GameObjectDisplayInfo.dbc");
const SQL_PATH = path.join(PATCH_DIR, "gameobject_template_trinity_atakke_edit.sql");
const OUTPUT_PATH = path.join(MODULE_DIR, "addon", "PatchBuildingCatalog.ts");
const ENTRIES_OUTPUT_PATH = path.join(MODULE_DIR, "shared", "PatchBuildingEntries.ts");
const DATASCRIPTS_ENTRIES_OUTPUT_PATH = path.join(MODULE_DIR, "datascripts", "PatchBuildingEntries.ts");
const FIRST_BUILDING_ENTRY = 4000000;
const LAST_BUILDING_ENTRY = 4002169;
const BROKEN_BUILDING_ENTRY = 4000253; // SQL explicitly marks this model [PATCH NOT WORKING].
const EXPECTED_SOURCE_BUILDING_COUNT = 2094;
const EXPECTED_PREVIEWABLE_BUILDING_COUNT = 1569;
const EXPECTED_EXCLUDED_WMO_COUNT = 525;

function dbcString(buffer, offset) {
    let end = offset;
    while (end < buffer.length && buffer[end] !== 0) end++;
    return buffer.toString("utf8", offset, end);
}

function readDisplayPaths() {
    const buffer = fs.readFileSync(DBC_PATH);
    assert.equal(buffer.toString("ascii", 0, 4), "WDBC", "Invalid DBC magic");

    const rowCount = buffer.readUInt32LE(4);
    const fieldCount = buffer.readUInt32LE(8);
    const recordSize = buffer.readUInt32LE(12);
    assert.equal(fieldCount, 19, "Unexpected GameObjectDisplayInfo field count");
    assert.equal(recordSize, 76, "Unexpected GameObjectDisplayInfo record size");

    const stringsOffset = 20 + rowCount * recordSize;
    const paths = new Map();
    for (let i = 0; i < rowCount; i++) {
        const rowOffset = 20 + i * recordSize;
        const id = buffer.readUInt32LE(rowOffset);
        const stringOffset = buffer.readUInt32LE(rowOffset + 4);
        paths.set(id, dbcString(buffer, stringsOffset + stringOffset).replace(/\//g, "\\"));
    }
    return paths;
}

function parseSqlValues(raw) {
    const values = [];
    let value = "";
    let inString = false;

    for (let i = 0; i < raw.length; i++) {
        const ch = raw[i];
        if (ch === "\\" && inString && i + 1 < raw.length) {
            value += raw[++i];
        } else if (ch === "'") {
            inString = !inString;
        } else if (ch === "," && !inString) {
            values.push(value.trim());
            value = "";
        } else {
            value += ch;
        }
    }
    values.push(value.trim());
    return values;
}

function readPatchObjects(displayPaths) {
    const sql = fs.readFileSync(SQL_PATH, "utf8");
    const expectedDelete = `delete from \`gameobject_template\` where entry between ${FIRST_BUILDING_ENTRY} and ${LAST_BUILDING_ENTRY};`;
    assert.ok(sql.toLowerCase().includes(expectedDelete), "Patch building range changed; update the catalog bounds");
    const insert = /^INSERT INTO `gameobject_template` VALUES \((.*)\);$/gm;
    const records = [];
    const entries = new Set();
    let match;

    while ((match = insert.exec(sql)) !== null) {
        const values = parseSqlValues(match[1]);
        assert.equal(values.length, 35, `Unexpected value count for: ${match[0]}`);

        const entry = Number(values[0]);
        if (entry >= FIRST_BUILDING_ENTRY && entry < 5000000) {
            assert.ok(entry <= LAST_BUILDING_ENTRY, `Uncatalogued building entry ${entry}`);
        }
        if (
            entry < FIRST_BUILDING_ENTRY ||
            entry > LAST_BUILDING_ENTRY ||
            entry === BROKEN_BUILDING_ENTRY
        ) continue;
        const displayId = Number(values[2]);
        const modelPath = displayPaths.get(displayId);
        assert.ok(modelPath, `Missing display ${displayId} for gameobject ${entry}`);
        assert.ok(!entries.has(entry), `Duplicate gameobject entry ${entry}`);
        entries.add(entry);
        records.push({ entry, name: values[3], modelPath });
    }

    assert.equal(
        records.length,
        EXPECTED_SOURCE_BUILDING_COUNT,
        "Patch building range changed; review the catalog scope",
    );
    records.sort((a, b) => a.entry - b.entry);
    return records;
}

function renderCatalog(records) {
    const lines = [
        "/**",
        " * Generated by scripts/generate-patch-catalog.cjs from the patch's curated building/decor range.",
        " * WMO records are excluded because the WotLK PlayerModel widget cannot render them.",
        " * Entry 4000253 is excluded because the source marks it [PATCH NOT WORKING].",
        " * Flat triples keep the WotLK addon allocation small: entry, display name, model path.",
        " */",
        "export const PATCH_BUILDING_STRIDE = 3;",
        `export const PATCH_BUILDING_COUNT = ${records.length};`,
        "export const PATCH_BUILDINGS: (number | string)[] = [",
    ];

    for (const record of records) {
        lines.push(`    ${record.entry}, ${JSON.stringify(record.name)}, ${JSON.stringify(record.modelPath)},`);
    }
    lines.push("];", "");
    return lines.join("\n");
}

function renderEntries(records) {
    const lines = [
        "/**",
        " * Generated by scripts/generate-patch-catalog.cjs from the previewable patch models.",
        " * Used by livescripts as the allowlist and by datascripts for per-entry previews.",
        " */",
        "export const PATCH_BUILDING_ENTRIES: number[] = [",
    ];

    for (const record of records) lines.push(`    ${record.entry},`);
    lines.push(
        "];",
        "",
        "export function isPatchBuildingEntry(entry: number): boolean {",
        "    let low = 0;",
        "    let high = PATCH_BUILDING_ENTRIES.length - 1;",
        "    while (low <= high) {",
        "        const middle = Math.floor((low + high) / 2);",
        "        const candidate = PATCH_BUILDING_ENTRIES[middle];",
        "        if (candidate === entry) return true;",
        "        if (candidate < entry) low = middle + 1;",
        "        else high = middle - 1;",
        "    }",
        "    return false;",
        "}",
        "",
    );
    return lines.join("\n");
}

const sourceRecords = readPatchObjects(readDisplayPaths());
assert.ok(
    sourceRecords.every(record => /\.(?:m2|mdx|wmo)$/i.test(record.modelPath)),
    "Expected only M2/MDX/WMO model paths",
);
const records = sourceRecords.filter(record => /\.(?:m2|mdx)$/i.test(record.modelPath));
const wmoCount = sourceRecords.length - records.length;
assert.equal(records.length, EXPECTED_PREVIEWABLE_BUILDING_COUNT, "Previewable patch model count changed");
assert.equal(wmoCount, EXPECTED_EXCLUDED_WMO_COUNT, "Excluded WMO count changed");
const output = renderCatalog(records);
const entriesOutput = renderEntries(records);

if (process.argv.includes("--check")) {
    assert.equal(fs.readFileSync(OUTPUT_PATH, "utf8"), output, "PatchBuildingCatalog.ts is stale");
    assert.equal(
        fs.readFileSync(ENTRIES_OUTPUT_PATH, "utf8"),
        entriesOutput,
        "PatchBuildingEntries.ts is stale",
    );
    assert.equal(
        fs.readFileSync(DATASCRIPTS_ENTRIES_OUTPUT_PATH, "utf8"),
        entriesOutput,
        "datascripts/PatchBuildingEntries.ts is stale",
    );
} else {
    fs.writeFileSync(OUTPUT_PATH, output, "utf8");
    fs.writeFileSync(ENTRIES_OUTPUT_PATH, entriesOutput, "utf8");
    fs.writeFileSync(DATASCRIPTS_ENTRIES_OUTPUT_PATH, entriesOutput, "utf8");
}

console.log(`Patch catalog: ${records.length} M2/MDX objects (${wmoCount} WMO excluded)`);
